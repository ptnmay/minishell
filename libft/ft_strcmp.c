#include "libft.h"

int	ft_strcmp(const char *str1, const char *str2)
{
	unsigned const char	*s1;
	unsigned const char	*s2;

	s1 = (unsigned const char *)str1;
	s2 = (unsigned const char *)str2;
	while (*s1 && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	return (*(unsigned char *)s1 - *(unsigned char *)s2);
}
